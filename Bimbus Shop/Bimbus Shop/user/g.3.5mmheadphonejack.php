	<h1>3.5mm headphone jack</h1> 

	The 3.5mm headphone jack is an industry standard audio plug. Most commonly used for connecting a pair of stereo headphones to your smartphone or 'piping' your audio from your phone to an external amp either in your home or your car.<br><br>

	Dependent on the number of connector rings, headphones can also include a microphone.<br><br>

	The current trend is for high-end smartphone flagships to exclude the 3.5mm jack, with headphone support being provided via Bluetooth.<br><br>

	The exclusion of the 3.5mm jack is polarising with smartphone manufactures claiming that removing the jack saves space internally, allows for thinner phones while eliminating a potential ingress point for water.<br>
	
